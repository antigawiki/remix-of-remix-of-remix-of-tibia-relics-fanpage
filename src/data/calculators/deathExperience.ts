export const getLevelFromExperience = (exp: number): number => {
  if (exp < 0) return 1;
  
  for (const [level, data] of Object.entries(experienceData)) {
    if (exp >= data.min && exp <= data.max) {
      return parseInt(level);
    }
  }
  
  // Se ultrapassar level 300, retorna 300
  if (exp > experienceData[300].max) {
    return 300;
  }
  
  return 1;
};

export const getLevelProgress = (exp: number): number => {
  const level = getLevelFromExperience(exp);
  const data = experienceData[level];
  if (!data) return 0;
  
  const range = data.max - data.min;
  if (range === 0) return 0;
  
  return Math.min(100, Math.max(0, ((exp - data.min) / range) * 100));
};

export interface DeathResult {
  afterDeath: number;
  loss: number;
  retentionPercentage: number;
}

export const calculateDeathExperience = (
  experience: number,
  activeBlessings: string[]
): DeathResult => {
  // Base: perde 10% da XP
  let retentionPercentage = 0.90;
  
  // Cada blessing aumenta a retenção
  blessings.forEach(blessing => {
    if (activeBlessings.includes(blessing.id)) {
      retentionPercentage += blessing.bonus;
    }
  });
  
  // Garantir que não ultrapasse 100%
  retentionPercentage = Math.min(1, retentionPercentage);
  
  const afterDeath = Math.round(experience * retentionPercentage);
  const loss = experience - afterDeath;
  
  return { afterDeath, loss, retentionPercentage };
};
